2. 「CDN」という仕組みの正体
URLの中に cdn という文字が入っていますね。これは Content Delivery Network（コンテンツ・デリバリー・ネットワーク） の略です。
通常、ファイルを自分のパソコン（ローカル）に保存して読み込ませることもできますが、CDNを使うと以下のメリットがあります。
準備が不要: 自分でファイルをダウンロードしてフォルダに入れる手間がありません。この1行を書くだけで、世界中のプロが使っている最新のデザイン機能がすぐに使えます。
読み込みが速い: ユーザーに一番近い場所にある世界各地のサーバーからファイルが届くため、表示がスムーズになります。 -->

