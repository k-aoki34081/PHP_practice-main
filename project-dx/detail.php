<!-- 1. index.php と detail.php は「別の部屋」
これまで作ってきた head.php や sidebar.php は、どの部屋にもある「共通の家具（パーツ）」でした。しかし、index.php と detail.php は、役割の違う**「独立した部屋」**です。
index.php (検索・一覧の部屋)
ユーザーが最初に訪れる場所。
detail.php (個別分析の部屋)
ユーザーが特定のデータを選んだ後に、画面がパッと切り替わって移動する先の場所。

2. なぜインクルードしてはいけないのか
もし index.php の中に detail.php をインクルードしてしまうと、一つの画面に「検索結果」と「詳細分析」が上下にダラダラと並んでしまい、非常に使いにくい画面になってしまいます。
プロの開発では、以下のように使い分けます。
include するもの: どのページでも使い回す「部品」（ヘッダー、サイドバーなど）。
リンク で飛ばすもの: 役割が違う「別の画面」（一覧から詳細へ）。 -->
